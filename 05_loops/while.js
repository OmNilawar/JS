//while loop :- while loop is used to execute a block of code as long as the condition is true.
//syntax :- 
/* 
    while (consition)
    {
        codeblock
    }
    the code block will only execute until the condition is true.
*/

//ex -
let i = 0;
while(i < 10)
{
    console.log(i);
    i++; //increment the i value so it does not go into infinite loop.
}
